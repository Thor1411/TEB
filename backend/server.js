app.post('/api/documents/:id/sign-embedded', authenticate, async (req, res) => {
  try {
    const docId = req.params.id
    if (!isSafeDocId(docId)) return res.status(400).json({ error: 'Invalid document id' })

    const p12Path = process.env.PDF_SIGN_P12_PATH
    const passphrase = process.env.PDF_SIGN_P12_PASSPHRASE
    if (!p12Path) {
      return res.status(501).json({
        error: 'Embedded signing not configured (set PDF_SIGN_P12_PATH and PDF_SIGN_P12_PASSPHRASE)'
      })
    }

    const { meta, bytes } = await readDocBytes(vault, docId)
    if (meta.ownerId !== req.user.id) return res.status(403).json({ error: 'Forbidden' })

    const p12Buffer = await fs.readFile(p12Path)
    const signedBytes = await signPdfEmbedded({
      pdfBytes: bytes,
      p12Buffer,
      passphrase
    })

    // Store as a new version (signing should be the last step).
    const newId = crypto.randomUUID()
    const now = new Date().toISOString()
    await writeDoc(vault, newId, {
      bytes: signedBytes,
      ownerId: req.user.id,
      createdAt: now,
      updatedAt: now,
      pageCount: meta.pageCount,
      parentId: docId,
      embeddedSignature: true
    })

    await appendAuditEvent({
      auditDir: vault.auditDir,
      docId: newId,
      actor: req.user.id,
      action: 'sign-embedded',
      details: { parentId: docId }
    })

    return res.json({ id: newId })
  } catch (e) {
    return res.status(500).json({ error: e.message })
  }
})

