app.post('/api/documents', authenticate, upload.single('pdf'), async (req, res) => {
  let tempPath
  try {
    if (!req.file) return res.status(400).json({ error: 'No PDF file uploaded' })
    tempPath = req.file.path
    const inBytes = await fs.readFile(tempPath)

    const validation = await validatePdfWithQpdf({ pdfBytes: inBytes, workDir: WORK_DIR })
    if (!validation.ok) {
      return res.status(400).json({ error: `PDF failed structural validation: ${validation.error}` })
    }

    const { bytes: safeBytes, pageCount } = await sanitizePdfByRasterize({
      pdfBytes: inBytes,
      workDir: WORK_DIR,
      timeoutMs: PDFTOPPM_TIMEOUT_MS,
      maxPages: MAX_SANITIZE_PAGES
    })

    const docId = crypto.randomUUID()
    const now = new Date().toISOString()
    const meta = await writeDoc(vault, docId, {
      bytes: safeBytes,
      ownerId: req.user.id,
      createdAt: now,
      updatedAt: now,
      pageCount
    })

    await appendAuditEvent({
      auditDir: vault.auditDir,
      docId,
      actor: req.user.id,
      action: 'upload+sanitize',
      details: { pageCount }
    })

    return res.json({ id: meta.id, pageCount: meta.pageCount })
  } catch (e) {
    return res.status(500).json({ error: e.message })
  } finally {
    if (tempPath) await fs.unlink(tempPath).catch(() => {})
  }
})

