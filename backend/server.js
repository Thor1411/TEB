const runLibreOfficeConvertToPdf = async ({ inputPath, outDir, timeoutMs }) => {
  const candidates = ['libreoffice', 'soffice']

  const tryRun = (cmd) => new Promise((resolve, reject) => {
    const args = [
      '--headless',
      '--nologo',
      '--nofirststartwizard',
      '--norestore',
      '--convert-to', 'pdf',
      '--outdir', outDir,
      inputPath
    ]

    const child = spawn(cmd, args, { shell: false, windowsHide: true, stdio: ['ignore', 'ignore', 'pipe'] })

    let stderr = ''
    child.stderr?.on('data', (d) => {
      stderr += d.toString()
      if (stderr.length > 20_000) stderr = stderr.slice(-20_000)
    })

    const timer = timeoutMs ? setTimeout(() => {
      child.kill('SIGKILL')
      reject(new Error(`${cmd} timed out`))
    }, timeoutMs) : null

    child.on('error', (err) => {
      if (timer) clearTimeout(timer)
      reject(err)
    })

    child.on('close', (code) => {
      if (timer) clearTimeout(timer)
      if (code === 0) return resolve()
      reject(new Error(`${cmd} failed (code ${code}): ${stderr || 'unknown error'}`))
    })
  })

  let lastErr = null
  for (const cmd of candidates) {
    try {
      await tryRun(cmd)
      return
    } catch (e) {
      lastErr = e
      // ENOENT => binary not installed; try next candidate.
      if (e?.code === 'ENOENT') continue
    }
  }

  if (lastErr?.code === 'ENOENT') {
    throw new Error('LibreOffice is not installed (expected `libreoffice` or `soffice`). Install LibreOffice to enable PPT→PDF conversion.')
  }
  throw lastErr || new Error('LibreOffice conversion failed')
}

const runQpdf = async ({ jobDir, args, timeoutMs }) => {
  const useDocker = (process.env.SANDBOX_DOCKER || '1') !== '0'
  const hasDocker = useDocker ? await dockerAvailable() : false

  const runLocal = async () => {
    await new Promise((resolve, reject) => {
      const child = spawn('qpdf', args, { cwd: jobDir, shell: false, windowsHide: true, stdio: ['ignore', 'ignore', 'pipe'] })

      let stderr = ''
      child.stderr?.on('data', (d) => {
        stderr += d.toString()
        if (stderr.length > 20_000) stderr = stderr.slice(-20_000)
      })

      const timer = timeoutMs ? setTimeout(() => {
        child.kill('SIGKILL')
        reject(new Error('qpdf timed out'))
      }, timeoutMs) : null

      child.on('error', (err) => {
        if (timer) clearTimeout(timer)
        reject(err)
      })

      child.on('close', (code) => {
        if (timer) clearTimeout(timer)
        if (code === 0) return resolve()
        reject(new Error(`qpdf failed (code ${code}): ${stderr || 'unknown error'}`))
      })
    })
  }

  if (hasDocker) {
    const image = process.env.SANDBOX_IMAGE || 'secure-pdf-sanitizer:latest'
    try {
      await runInDockerSandbox({
        image,
        workDir: jobDir,
        cmd: 'qpdf',
        args,
        timeoutMs
      })
      return
    } catch {
      await runLocal()
      return
    }
  }

  await runLocal()
}

