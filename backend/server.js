const isSafeStoredFilename = (filename) => {
  // Stored filenames are generated by us: <uuid>.<ext>
  return typeof filename === 'string' && /^[0-9a-fA-F-]{20,}\.(pdf|png|jpg|jpeg)$/i.test(filename)
}

const getStoredFilename = (mimetype) => {
  let ext = 'bin'
  if (mimetype === 'application/pdf') ext = 'pdf'
  if (mimetype === 'image/png') ext = 'png'
  if (mimetype === 'image/jpeg' || mimetype === 'image/jpg') ext = 'jpg'

  const id = crypto.randomUUID()
  return `${id}.${ext}`
}

