const signShareToken = ({ docId, expMs }) => {
  const payload = { docId, expMs }
  const payloadB64 = Buffer.from(JSON.stringify(payload), 'utf8').toString('base64url')
  const sig = crypto.createHmac('sha256', SHARE_SECRET).update(payloadB64).digest('base64url')
  return `${payloadB64}.${sig}`
}

const verifyShareToken = (token) => {
  if (typeof token !== 'string') return null
  const [payloadB64, sig] = token.split('.')
  if (!payloadB64 || !sig) return null
  const expected = crypto.createHmac('sha256', SHARE_SECRET).update(payloadB64).digest('base64url')
  if (!crypto.timingSafeEqual(Buffer.from(sig), Buffer.from(expected))) return null
  const payload = JSON.parse(Buffer.from(payloadB64, 'base64url').toString('utf8'))
  if (!payload?.docId || !payload?.expMs) return null
  if (!isSafeDocId(payload.docId)) return null
  if (Date.now() > payload.expMs) return null
  return payload
}

