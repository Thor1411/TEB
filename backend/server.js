app.post('/api/documents/:id/redact', authenticate, async (req, res) => {
  try {
    const docId = req.params.id
    if (!isSafeDocId(docId)) return res.status(400).json({ error: 'Invalid document id' })

    const meta = await readDocMeta(vault, docId)
    if (meta.ownerId !== req.user.id) return res.status(403).json({ error: 'Forbidden' })

    const redactions = Array.isArray(req.body?.redactions) ? req.body.redactions : []
    const byPage = {}
    for (const r of redactions) {
      const page = Number(r?.page)
      if (!Number.isFinite(page) || page < 1 || page > MAX_SANITIZE_PAGES) continue
      const rects = Array.isArray(r?.rects) ? r.rects : []
      byPage[page] = rects
        .filter(x => x && [x.x, x.y, x.width, x.height].every(Number.isFinite))
        .map(x => ({
          x: Math.max(0, Math.min(1, x.x)),
          y: Math.max(0, Math.min(1, x.y)),
          width: Math.max(0, Math.min(1, x.width)),
          height: Math.max(0, Math.min(1, x.height))
        }))
    }

    const { bytes } = await readDocBytes(vault, docId)
    const validation = await validatePdfWithQpdf({ pdfBytes: bytes, workDir: WORK_DIR })
    if (!validation.ok) {
      return res.status(400).json({ error: `PDF failed structural validation: ${validation.error}` })
    }
    const { bytes: redactedBytes, pageCount } = await redactPdfByRasterize({
      pdfBytes: bytes,
      workDir: WORK_DIR,
      timeoutMs: PDFTOPPM_TIMEOUT_MS,
      maxPages: MAX_SANITIZE_PAGES,
      redactionsByPage: byPage
    })

    const newId = crypto.randomUUID()
    const now = new Date().toISOString()
    const newMeta = await writeDoc(vault, newId, {
      bytes: redactedBytes,
      ownerId: req.user.id,
      createdAt: now,
      updatedAt: now,
      pageCount,
      parentId: docId
    })

    await appendAuditEvent({
      auditDir: vault.auditDir,
      docId,
      actor: req.user.id,
      action: 'redact->newVersion',
      details: { newId, count: redactions.length }
    })

    await appendAuditEvent({
      auditDir: vault.auditDir,
      docId: newId,
      actor: req.user.id,
      action: 'createRedactedVersion',
      details: { parentId: docId, pageCount }
    })

    return res.json({ id: newMeta.id, pageCount: newMeta.pageCount, parentId: docId })
  } catch (e) {
    return res.status(500).json({ error: e.message })
  }
})

